CUBE_NUMBERS_TO_XYZ = (
    (4, 1, 1),  # 1
    (3, 1, 1),  # 2
    (2, 1, 1),  # 3
    (1, 1, 1),  # 4
    (4, 0, 1),  # 5
    (3, 0, 1),  # 6
    (2, 0, 1),  # 7
    (1, 0, 1),  # 8
    (3, 0, 0),  # 9
    (2, 0, 0),  # 10
    (3, 1, 0),  # 11
    (2, 1, 0),  # 12
)

ALWAYS_INCLUDED_XYZS = (
    (0, 0, 0), (1, 0, 0), (4, 0, 0), (5, 0, 0),  # y=0, z=0
    (0, 1, 0), (1, 1, 0), (4, 1, 0), (5, 1, 0),  # y=1, z=0
    (0, 0, 1), (5, 0, 1),   # y=0, z=1
    (0, 1, 1), (5, 1, 1)    # y=1, z=1
)


def binary_string_to_cube_numbers(binary_string: str) -> set[int]:
    binary_list = [int(letter) for letter in binary_string.replace(' ', '')]
    return set([ndx + 1 for ndx, has_cube in enumerate(
        binary_list) if has_cube == 1])


def cube_numbers_to_xyzs(set_of_cubes: set[int]) -> tuple[tuple[int]]:
    other_xyzs = []
    for cube_number in set_of_cubes:
        other_xyzs.append(CUBE_NUMBERS_TO_XYZ[cube_number - 1])
    all_xyzs = (*ALWAYS_INCLUDED_XYZS, *other_xyzs)
    return all_xyzs


def has_all_always_included_xyzs(xyzs: tuple[tuple[int]]) -> bool:
    return set(ALWAYS_INCLUDED_XYZS).issubset(set(xyzs))


def xyzs_to_cube_numbers(xyzs: tuple[tuple[int]]) -> set[int]:
    cubes = []
    for xyz in xyzs:
        if xyz in CUBE_NUMBERS_TO_XYZ:
            cubes.append(CUBE_NUMBERS_TO_XYZ.index(xyz)+1)
    return cubes


def cube_numbers_to_binary_string(set_of_cubes: set[int]) -> str:
    binary_list = ['1' if (i+1) in set_of_cubes else '0' for i in range(12)]
    bin_str = ''.join(binary_list)
    return f'{bin_str[0:4]} {bin_str[4:8]} {bin_str[8:10]} {bin_str[10:12]}'


